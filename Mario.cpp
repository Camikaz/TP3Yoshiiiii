int Mario ::nb_mario_ = 0;

// ===========================================================================
//                       Definition of static attributes
// ===========================================================================

// ===========================================================================
//                                Constructors
// ===========================================================================
Mario::Mario() {
	nb_mario_ ++;
}

// ===========================================================================
//                                 Destructor
// ===========================================================================
Mario::~Mario() {
	nb_mario_ --;
}

// ===========================================================================
//                               Public Methods
// ===========================================================================

void Mario::Break() {
  if (*speed_ < 0.1) *speed_ = 0.;
  else *speed_ *= 0.8;
}

void Mario::WhatAmI() const{
	std::cout<<" It's me Maaario !\n" <<std::endl;
}

